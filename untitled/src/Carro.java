public class Carro {

    public String marca;
    public String modelo;
    public int velocidadeMaxima;
    public int ano;

    public Carro() {
    }

    public Carro(String marca, String modelo, int velocidadeMaxima, int ano) {
        this.marca = marca;
        this.modelo = modelo;
        this.velocidadeMaxima = velocidadeMaxima;
        this.ano = ano;
    }
}
